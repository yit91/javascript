function cobaSwitch() {
  var nilai =5;
  switch (nilai) {
    case 1:
      console.log("Senin");
      break;
      case 2:
        console.log("Selasa");
        break;
        case 3:
          console.log("Rabu");
          break;
    default:
      console.log("Lanjutkan");
  }
}
cobaSwitch()
